const mongoose = require('mongoose');

const { Schema } = mongoose;

const schoolSchema = new Schema(
    {
    schoolID: {
      type: Number,
      required: true,
      unique: true
    },
    name: {
      type: String,
      required: true
    },
    type: {
      type: String,
      required: true
    },
    address: {
      type: String,
      required: true
    },
    students: [{
      type: mongoose.Schema.Types.ObjectId,
      ref: 'Student'
    }],
    teachers: [{
      type: mongoose.Schema.Types.ObjectId,
      ref: 'Teacher'
    }]
    },
    { timestamps: true}
);

export default mongoose.model('School', schoolSchema);